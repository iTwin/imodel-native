// Inline methods for class Chronometer
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
// Class construtor
//-----------------------------------------------------------------------------

inline Chronometer::Chronometer()
{
	m_finish = m_start;
}

//-----------------------------------------------------------------------------
// Public 
//-----------------------------------------------------------------------------

inline void Chronometer::Start()
{
    m_start = clock();
}

//-----------------------------------------------------------------------------
// Public 
//-----------------------------------------------------------------------------

inline WString Chronometer::Stop()
{
   m_finish = clock();

   return GetTime();
}

//-----------------------------------------------------------------------------
// Public 
//-----------------------------------------------------------------------------

inline WString Chronometer::GetTime()
{
   double  duration;
   duration = (double)(m_finish - m_start) / CLOCKS_PER_SEC;
   WChar  buffer[200];
   _stprintf(buffer, _TEXT("%2.1f seconds\n"), duration);

   return WString(buffer);
}

