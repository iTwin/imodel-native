//-----------------------------------------------------------------------------
// operator++
//-----------------------------------------------------------------------------
inline const HFCPtr<HRARaster>& HRABitmapIterator::Next()
    {
    m_Index++;

    // Return a null raster
    return m_pNullRaster;
    }

//-----------------------------------------------------------------------------
// operator()
//-----------------------------------------------------------------------------
inline const HFCPtr<HRARaster>& HRABitmapIterator::operator()()
    {
    if (m_Index == 0L)
        return (GetRaster());
    else
        return m_pNullRaster;
    }


//-----------------------------------------------------------------------------
// Reset
//-----------------------------------------------------------------------------
inline void HRABitmapIterator::Reset()
    {
    m_Index = 0L;
    }
