#ifndef POINTOOLS_UNITS_H
#define POINTOOLS_UNITS_H

#define SECOND              (1.0)
#define MINUTE              (60 * SECOND)
#define HOUR                (60 * MINUTE)
#define DAY                 (24 * HOUR)
#define SUNRISE             (DAY / 4)
#define SUNSET              (DAY * 3 / 4)
#define MIDNIGHT            (0)

#define CENTIMETER          (0.01)
#define DECIMETER           (0.1)
#define METER               (1.0)
#define KILOMETER			(1000.0)

#endif