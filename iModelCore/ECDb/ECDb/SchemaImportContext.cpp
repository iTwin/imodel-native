//---------------------------------------------------------------------------------------
bool SchemaImportECDbMapDb::IsNameInUse(Utf8CP name) const
    {
    return m_usedIndexNames.find(name) != m_usedIndexNames.end() || m_coreMapDb.IsNameInUse(name);
    }


