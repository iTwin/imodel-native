// @bsimethod                                                    Affan.Khan        10/2014
//---------------------------------------------------------------------------------------
BentleyStatus SchemaImportECDbMapDb::CreateInDb(ECDbR ecdb, ECDbSqlIndex const& index) const
    {
    if (index.GetColumns().empty())
        {
        BeAssert(false && "Index definition is not valid");
        return ERROR;
        }

    bool exists = false;
        {
        CachedStatementPtr stmt = nullptr;
        if (BE_SQLITE_OK != ecdb.GetCachedStatement(stmt, "select NULL from sqlite_master WHERE type='index' and name=?"))
            {
            BeAssert(false);
            return ERROR;
            }

        stmt->BindText(1, index.GetName().c_str(), Statement::MakeCopy::No);
        exists = BE_SQLITE_ROW == stmt->Step();
        }

    if (exists)
        {
        Utf8String ddl("DROP INDEX [");
        ddl.append(index.GetName()).append("]");

        if (ecdb.ExecuteSql(ddl.c_str()) != BE_SQLITE_OK)
            {
            ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Error, "Failed to drop index %s on table %s. Error: %s", index.GetName().c_str(), index.GetTable().GetName().c_str(),
                                                          ecdb.GetLastError());

            BeAssert(false && "Failed to create index");
            return ERROR;
            }
        }

    NativeSqlBuilder ddl;
    if (SUCCESS != BuildCreateIndexDdl(ddl, ecdb, index))
        return ERROR;

    if (BE_SQLITE_OK != ecdb.ExecuteSql(ddl.ToString()))
        {
        ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Error, "Failed to create index %s on table %s. Error: %s", index.GetName().c_str(), index.GetTable().GetName().c_str(),
                                                      ecdb.GetLastError());
        BeAssert(false && "Failed to create index");
        return ERROR;
        }

    return SUCCESS;
    }

//---------------------------------------------------------------------------------------
// @bsimethod                                                Krischan.Eberle 10/2015
//---------------------------------------------------------------------------------------
BentleyStatus SchemaImportECDbMapDb::BuildCreateIndexDdl(NativeSqlBuilder& ddl, ECDbCR ecdb, ECDbSqlIndex const& index) const
    {
    ddl.Append("CREATE ");
    if (index.GetIsUnique())
        ddl.Append("UNIQUE ");

    Utf8CP indexName = index.GetName().c_str();
    Utf8CP tableName = index.GetTable().GetName().c_str();
    ddl.Append("INDEX ").AppendEscaped(indexName).Append(" ON ").AppendEscaped(tableName).Append(" (");

    bool isFirstCol = true;
    for (ECDbSqlColumn const* col : index.GetColumns())
        {
        if (!isFirstCol)
            ddl.AppendComma(false);

        ddl.AppendEscaped(col->GetName().c_str());
        isFirstCol = false;
        }

    ddl.AppendParenRight();

    NativeSqlBuilder whereClause;
    if (SUCCESS != GenerateIndexWhereClause(whereClause, ecdb, index))
        return ERROR;

    if (!whereClause.IsEmpty())
        ddl.Append(" WHERE ").Append(whereClause.ToString());

    return SUCCESS;
    }

//---------------------------------------------------------------------------------------
// @bsimethod                                                    Krischan.Eberle  10/2015
//---------------------------------------------------------------------------------------
BentleyStatus SchemaImportECDbMapDb::GenerateIndexWhereClause(NativeSqlBuilder& whereClause, ECDbCR ecdb, ECDbSqlIndex const& index) const
    {
    bool hasWhere = !index.GetPrimaryWhereClause().empty();
    if (hasWhere)
        whereClause.AppendParenLeft().Append(index.GetPrimaryWhereClause().c_str()).AppendParenRight();

    ECDbSqlColumn const* classIdCol = nullptr;
    if (!index.HasClassId() || !index.GetTable().TryGetECClassIdColumn(classIdCol))
        return SUCCESS;

    BeAssert(index.HasClassId());

    BeAssert(classIdCol != nullptr);

    ECClassCP ecclass = ecdb.Schemas().GetECClass(index.GetClassId());
    if (ecclass == nullptr)
        {
        BeAssert(false);
        return ERROR;
        }

    ClassMapCP classMap = ecdb.GetECDbImplR().GetECDbMap().GetClassMapCP(*ecclass);
    if (classMap == nullptr)
        {
        BeAssert(false);
        return ERROR;
        }

    StorageDescription const& storageDescription = classMap->GetStorageDescription();
    std::vector<size_t> nonVirtualPartitionIndices = storageDescription.GetNonVirtualHorizontalPartitionIndices();
    HorizontalPartition const* horizPartition = nullptr;
    if (nonVirtualPartitionIndices.empty())
        horizPartition = &storageDescription.GetRootHorizontalPartition();
    else
        {
        BeAssert(nonVirtualPartitionIndices.size() == 1 && "Check that class only maps to a single table should have been done during class name preparation");
        horizPartition = storageDescription.GetHorizontalPartition(nonVirtualPartitionIndices[0]);
        }

    if (!horizPartition->NeedsClassIdFilter())
        return SUCCESS;

    //now we know the index would have to be partial.
    if (!index.GetIsUnique())
        {
        //non-unique indexes will never be made partial as they don't enforce anything and usually
        //are just there for performance - which a partial index will spoil.
        if (ecclass->GetRelationshipClassCP() != nullptr)
            ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Warning,
                                                          "ECDb creates an index for ECRelationshipClass '%s' on the foreign key column which is shared by multiple classes. "
                                                          "For performance reasons ECDb will create a non-partial index which therefore might include unnecessary rows."
                                                          "If you don't need that index, you can disable it in the ECRelationshipClass (via custom attribute).", ecclass->GetFullName());

        else
            ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Warning,
                                                          "Index '%s' defined for ECClass '%s' includes a column shared by multiple ECClasses. "
                                                          "For performance reasons, ECDb will create a non-partial index which therefore includes more rows than needed.",
                                                          index.GetName().c_str(), ecclass->GetFullName());

        return SUCCESS;
        }

    //unique indexes are always created to not lose enforcement of the uniqueness
    if (!index.IsAutoGenerated())
        {
        ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Warning,
                                                      "Index '%s' defined for ECClass '%s' includes a column shared by multiple ECClasses. "
                                                      "This results in a partial index filtered by ECClassId which might impact performance. "
                                                      "Consider changing the ECClass to not share columns.", index.GetName().c_str(), ecclass->GetFullName());
        }
    else
        {
        if (ecclass->GetRelationshipClassCP() != nullptr)
            ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Warning,
                                                      "ECDb has to create a unique index for ECRelationshipClass '%s' to enforce the cardinality. "
                                                      "This results in a partial index filtered by ECClassId which might impact performance. "
                                                      "Consider changing the ECRelationshipClass to not share a table.", ecclass->GetFullName());
        }

    if (hasWhere)
        whereClause.AppendSpace().Append(BooleanSqlOperator::And, true);

    if (!index.AppliesToSubclassesIfPartial())
        {
        whereClause.Append(classIdCol->GetName().c_str()).Append(BooleanSqlOperator::EqualTo).Append(index.GetClassId());
        return SUCCESS;
        }


    if (hasWhere)
        whereClause.AppendParenLeft();

    horizPartition->AppendECClassIdFilterSql(classIdCol->GetName().c_str(), whereClause);
    
    if (hasWhere)
        whereClause.AppendParenRight();

    return SUCCESS;
    }


