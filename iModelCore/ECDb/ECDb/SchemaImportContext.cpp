// @bsimethod                                                    Krischan.Eberle   10/2015
//---------------------------------------------------------------------------------------
BentleyStatus SchemaImportECDbMapDb::Load()
    {
    //first load generic mapping meta data
    ECDbSQLManager& manager = m_coreMapDb.GetManagerR();
    if (!manager.IsLoaded())
        {
        if (SUCCESS != manager.Load())
            return ERROR;
        }
    
    //then load meta data only needed for schema import
    return ReadIndexInfosFromDb(manager.GetECDb());
    }

//---------------------------------------------------------------------------------------
// @bsimethod                                                    Affan.Khan        09/2014
//---------------------------------------------------------------------------------------
ECDbSqlIndex* SchemaImportECDbMapDb::CreateIndex(ECDbCR ecdb, ECDbSqlTable& table, Utf8CP indexName, bool isUnique, std::vector<ECDbSqlColumn const*> const& columns, Utf8CP primaryWhereClause, bool isAutoGenerated, ECN::ECClassId classId, bool applyToSubclassesIfPartial)
    {
    if (columns.empty())
        {
        BeAssert(false && "Index must have at least one column defined.");
        return nullptr;
        }

    Utf8String generatedIndexName;
    if (Utf8String::IsNullOrEmpty(indexName))
        {
        do
            {
            m_coreMapDb.GetNameGenerator().Generate(generatedIndexName);
            } while (IsNameInUse(generatedIndexName.c_str()));

            indexName = generatedIndexName.c_str();
        }

    return CacheIndex(ecdb, m_coreMapDb.GetManagerR().GetIdGenerator().NextIndexId(),
                      table, indexName, isUnique, columns, primaryWhereClause, isAutoGenerated, classId, applyToSubclassesIfPartial);
    }

//---------------------------------------------------------------------------------------
// @bsimethod                                                    Affan.Khan        09/2014
//---------------------------------------------------------------------------------------
ECDbSqlIndex* SchemaImportECDbMapDb::CreateIndex(ECDbCR ecdb, ECDbSqlTable& table, Utf8CP indexName, bool isUnique, std::vector<Utf8CP> const& columnNames, Utf8CP primaryWhereClause, bool isAutoGenerated, ECN::ECClassId classId, bool applyToSubclassesIfPartial)
    {
    if (columnNames.empty())
        return nullptr;

    std::vector<ECDbSqlColumn const*> columns;
    for (Utf8CP colName : columnNames)
        {
        ECDbSqlColumn const* col = table.FindColumnCP(colName);
        if (col == nullptr)
            {
            BeAssert(false && "Failed to find index column");
            return nullptr;
            }

        columns.push_back(col);
        }

    return CreateIndex(ecdb, table, indexName, isUnique, columns, primaryWhereClause, isAutoGenerated, classId, applyToSubclassesIfPartial);
    }

//---------------------------------------------------------------------------------------
// @bsimethod                                                    Affan.Khan        09/2014
//---------------------------------------------------------------------------------------
ECDbSqlIndex* SchemaImportECDbMapDb::CacheIndex(ECDbCR ecdb, ECDbIndexId id, ECDbSqlTable& table, Utf8CP indexName, bool isUnique, std::vector<ECDbSqlColumn const*> const& columns, Utf8CP primaryWhereClause, bool isAutoGenerated, ECN::ECClassId classId, bool applyToSubclassesIfPartial) const
    {
    BeAssert(!columns.empty());

    auto it = m_usedIndexNames.find(indexName);
    if (it != m_usedIndexNames.end())
        {
        ecdb.GetECDbImplR().GetIssueReporter().Report(ECDbIssueSeverity::Error, "Index with name '%s' already defined in the ECDb file.", indexName);
        return nullptr;
        }

    std::unique_ptr<ECDbSqlIndex> index (new ECDbSqlIndex(id, table, indexName, isUnique, columns, primaryWhereClause, isAutoGenerated, classId, applyToSubclassesIfPartial));
    ECDbSqlIndex* indexP = index.get();
    m_indexes.push_back(std::move(index));

    m_usedIndexNames.insert(indexP->GetName().c_str());
    return indexP;
    }



