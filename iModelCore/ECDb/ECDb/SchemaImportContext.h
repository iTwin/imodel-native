    BentleyStatus Load();

    ECDbSqlIndex* CreateIndex(ECDbSqlTable&, Utf8CP indexName, bool isUnique, std::vector<ECDbSqlColumn const*> const&, Utf8CP primaryWhereClause, bool isAutoGenerated, ECN::ECClassId, bool applyToSubclassesIfPartial = true);
